// 四、轨迹生成器 (已验证在浏览器中可用)
// ============================================================
function generateTrajectory(targetX, targetY = 0) {
  const list = [[0, 0, 0]];
  let cx = 0, cy = 0;
  
  // Phase 1: 快速启动 (0-15%)
  for (let i = 0; i < 12; i++) {
    cx += Math.floor(targetX * 0.15 / 12) + Math.floor(Math.random() * 2);
    cy += (Math.random() - 0.5) * 0.6;
    const dt = 100 - i * 5 + Math.floor(Math.random() * 30);
    if (cx <= targetX) list.push([cx, Math.round(cy), Math.max(5, dt)]);
  }
  
  // Phase 2: 匀速 (15-80%)
  const midEnd = Math.floor(targetX * 0.80);
  while (cx < midEnd) {
    cx += 2 + Math.floor(Math.random() * 2);
    cy += (Math.sin(cx * 0.04) * 0.4 + (Math.random() - 0.5) * 0.3);
    list.push([Math.min(cx, targetX), Math.round(cy), 5 + Math.floor(Math.random() * 2)]);
  }
  
  // Phase 3: 减速 (80-95%)
  const fineEnd = Math.floor(targetX * 0.95);
  while (cx < fineEnd) {
    cx += 1;
    cy += (Math.random() - 0.5) * 0.3;
    list.push([Math.min(cx, targetX), Math.round(cy), 8 + Math.floor(Math.random() * 8)]);
  }
  
  // Phase 4: 微调对齐 (95-100%)
  while (cx < targetX) {
    cx += 1;
    cy += Math.round(cy * -0.2 + (Math.random() - 0.5) * 0.1);
    list.push([cx, Math.round(cy), 20 + Math.floor(Math.random() * 30)]);
  }
  
  if (list[list.length - 1][0] !== targetX) {
    list.push([targetX, Math.round(cy), 350 + Math.floor(Math.random() * 150)]);
  }
  
  return list;
}

